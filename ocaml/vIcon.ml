  | `Event        -> date
  | `Group        -> folder_user
  | `Subscription -> user_go
  | `Forum        -> comments
  | `Poll         -> chart_bar
  | `Album        -> images
  | `Course       -> book_open

let empty_of_entity_kind : MEntityKind.t -> string = function
  | `Event        -> Large.calendar
